     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate([
            'name' => 'required',
            'gender' => 'required',
            'address' => 'required',
            'phone' => 'required'
            
        ]);
    
        Citizens::create($request->all());
     
        return redirect()->route('dashboard')
                        ->with('success','citizens created successfully.');
    
    }

